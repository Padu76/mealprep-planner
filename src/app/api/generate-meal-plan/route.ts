  console.log('üìã Raw form data:', JSON.stringify(formData, null, 2));

  // Normalizzazione dati con allergie/preferenze
  const normalizedData = normalizeFormDataWithAllergies(formData);
  console.log('üìä Dati normalizzati:', JSON.stringify(normalizedData, null, 2));

