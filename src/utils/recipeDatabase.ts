        if (!recipe.nome.toLowerCase().includes(query) &&
            !recipe.ingredienti.some(ing => ing.toLowerCase().includes(query)) &&
            !recipe.tags.some(tag => tag.toLowerCase().includes(query))) {
          return false;
        }
